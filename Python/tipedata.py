print('---Belajar Tipe Data---')

# Tipe Data Integer
x = 5
y = 3
z = x + y
print(z)

# Tipe Data Float
a = 7.5
print(a)

# Menggunakan single quote
myFirstName = 'Tubagus Muhamad'

# Menggunakan double quote
myLastName = "Ihsan Febrian"

# Penulisan Angka sebagai String
myAge = "18"

# Penulisan string multiline
myHobby = """Hobi saya adalah Traveling."""

# Mencetak 2 variabel string dengan perintah print
print(myFirstName + " " + myLastName)
print("Umur saya " + myAge)
print("Hobi saya " + myHobby)

# Contoh penulisan boolean yang benar
a = True
b = False

# Contoh penulisan boolean yang benar (bukan error)
c = a

print(c)
